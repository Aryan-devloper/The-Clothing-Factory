        // Search functionality
        function searchCards() {
            var input, filter, cards, card, title, text, i, txtValue;
            input = document.getElementById("searchInput");
            filter = input.value.toUpperCase();
            cards = document.getElementById("cardContainer").getElementsByClassName("card-item");

            for (i = 0; i < cards.length; i++) {
                card = cards[i];
                title = card.getElementsByClassName("card-title")[0];
                text = card.getElementsByClassName("card-text")[0];
                txtValue = (title.textContent || title.innerText) + (text.textContent || text.innerText);
                
                if (txtValue.toUpperCase().indexOf(filter) > -1) {
                    card.style.display = "";
                } else {
                    card.style.display = "none";
                }
            }
        }
    </script>

